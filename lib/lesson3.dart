//Operators
void main() {
  //Arithmtic Operator
  print(44+4);
  print(44-4);
  print(44*4);
  print(44/4);

  //Equality operator
  print(44==42);
  print(44!=42);

  //Increment and Decrement Operator
  var number=5;
  print(++number);
  print(number++);

  var nextNumber=4;
  print(--nextNumber);
  print(nextNumber--);

  //Compound Assignment Operator
  var firstNumber=5;
  firstNumber+=1;
  print(firstNumber);

  var secondNumber=5;
  secondNumber-=1;
  print(secondNumber);

  //Comparison operator
  print(23<32);
  print(23<=32);
  print(33>33);
  print(33>=33);

  //Modulo operator
  print(33%3);

  //Logical Operator
  print(25<24 && 32>23);
  print(25<24 || 32>23);

  //Negation Operator
  print(!(24>25));

}
